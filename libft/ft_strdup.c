#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*dest;
	size_t	i;

	i = 0;
	len = ft_strlen(s);
	dest = malloc(len + 1);
	if (!dest)
	{
		return (NULL);
	}
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
